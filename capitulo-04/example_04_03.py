object_a = 10
object_b = 50

print(f'10 > 0: {object_a > 0}.')
print(f'50 <= 0: {object_b <= 0}.')
print(f'10 >= 50: {object_a >= object_b}.')
print(f'10 < 50: {object_a < object_b}.')
print(f'10 <= 50: {object_a <= object_b}.')
print(f'5 * 10 == 50: {(5 * object_a) == object_b}.')
print(f'5 * 10 < 50: {(5 * object_a) < object_b}.')
print(f'10 >= √50: {object_a >= pow(object_b, 0.5)}.')
print(f'√50: {pow(object_b, 0.5)}.')